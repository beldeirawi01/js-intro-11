const animals = ['Lion', 'Bird', 'Tiger'];

// Push adds elements to the end of the array

animals.push('Monkey');
animals.push('Fish');
console.log(animals.push('Cat')); // outputs length
console.log(animals); // outputs the whole array



// Pop removes elements starting from the last index

const numbers = [5, 10, 100, 200, 4, 13];

console.log(numbers.pop()); // tells you the value of what it is removing
numbers.pop(); // removes 4
numbers.pop(); // removes 200

console.log(numbers); // outputs updated array




