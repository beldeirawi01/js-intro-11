// For loop 
for (let i = 0; i <= 50; i++) {
    if (i % 2 === 0) {
        console.log(i)
    }
}



// while loop
let nums = 0;
while (nums <= 50) {
    if (nums % 2 === 0) {
        console.log(nums);
    }
    nums++;
}




