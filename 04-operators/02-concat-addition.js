
let n1 = 3;
let n2 = '5';
let n3 = 4;
let n4 = '10';

//Adding number and string is concatenation
console.log(n1 + n2) // '35

// Multiplying or Dividing string and number equals a number
console.log(n2 * n3) //20

console.log(n4 / n2); // 5


// Empty strings always evaluate to 0 
console.log(5 * '') // 0


